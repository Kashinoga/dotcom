              <p>I gave myself a little under an hour to come up with this list. In this time, I found it incredibly hard to                  think about and to remember the highlights of my year—without having to resort to my journal or my photos. I think it reinforces how important it is for me to relax and record from time to time.</p>

<p>Those familiar with me may have noticed that I have shared less and less of these as the year has gone on. It’s much easier to sit down and write when I’m sad—it offers a sense of letting go of that sadness into the void. Perhaps this is an indicator that I’m doing much better this year.</p>

<p>Those unfamiliar with me: I encountered major grief about two years ago. I threw myself into psychiatry and therapy, and I started to write and to share these letters with a small group of people. It’s a little embarrassing and cringey to go back and to read some of my earlier letters, but I definitely feel like expressing and sharing my innermost turmoils and jubilations has immensely helped me move forward with my grief.</p>

<p>Even with the help that I’ve received, the tools and techniques that I’ve acquired and learned to use, and the emotional growth that I’ve nurtured this year: I still make mistakes and know I’ll continue to make them. Though, it’s no excuse to not keep growing, to seek genuine forgiveness, and to do markedly better. To be better. To be best. (Sorry, I’m going to keep sneaking that line in.)</p>

<p>If you’re reading this, please know that you’ve helped me in some way this past year. I hope I’ve been able to give back in some useful way as a token of my appreciation.</p>

<p>I hope the year has treated you fairly, and I wish you all the best in 2025.</p>

<p>Stay safe. Look up.</p>
